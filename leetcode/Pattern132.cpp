class Solution {
public:
  bool find132pattern(vector<int> &a) {
    int n = a.size();
    if (n < 3)
      return 0;
    for (int i = 0; i < n - 2; i++) {
      if (a[i + 1] == (a[i]))
        continue;
      for (int j = i + 1; j < n - 1; j++) {
        if (a[j] <= (a[i] + 1))
          continue;
        for (int k = j + 1; k < n; k++) {
          if ((a[k] <= a[i]) || (a[k] >= a[j]))
            continue;
          // return 1;
          cout << a[i] << " " << a[j] << " " << a[k] << endl;
        }
      }
    }
    return 0;
  }
};

